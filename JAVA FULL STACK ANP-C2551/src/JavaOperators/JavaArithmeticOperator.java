package JavaOperators;

public class JavaArithmeticOperator {

	public static void main(String[] args) {
	
		int a =30;
		int b =10;
		
		//Addition
		int sum = a+b;
		System.out.println("The addition of (a + b) is : " + sum);
		
		//Substraction
				int sub= a-b;
				System.out.println("The substractio of (a - b) is : " + sub);

				//Multiplication
				int mul = a*b;
				System.out.println("The multiplication of (a * b) is : " + mul);
				
				//Division
				int div = a/b;
				System.out.println("The division of (a / b) is : " + div);

				//Module
				int mod = a%b;
				System.out.println("The module of (a % b) is : " +mod);
				

	}

}
